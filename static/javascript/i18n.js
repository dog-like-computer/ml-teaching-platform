// 获取当前语言设置
let currentLanguage = 'cn';

// 初始化语言设置
async function initLanguage() {
    try {
        const response = await fetch('/api/get_language');
        const data = await response.json();
        if (data.status === 1) {
            currentLanguage = data.msg;
        }
    } catch (error) {
        console.error('Error getting language setting:', error);
    }
    
    // 应用语言设置
    applyLanguage();
}

// 应用语言设置
function applyLanguage() {
    // 选择所有带有data-i18n属性的元素
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = i18n[currentLanguage][key];
        if (translation) {
            // 处理不同类型的元素
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else if (element.tagName === 'OPTION') {
                element.textContent = translation;
            } else {
                element.textContent = translation;
            }
        }
    });
    
    // 更新页面标题
    const titleKey = document.querySelector('title[data-i18n]');
    if (titleKey) {
        const titleTranslation = i18n[currentLanguage][titleKey.getAttribute('data-i18n')];
        if (titleTranslation) {
            document.title = titleTranslation;
        }
    }
}

// 获取翻译
function t(key) {
    return i18n[currentLanguage][key] || key;
}

// 初始化
window.addEventListener('DOMContentLoaded', initLanguage);